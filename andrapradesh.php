<h1 id = "ap"  style = "color:orange">ABOUT AP</h1>
<p style = "font-size:200%"><i>
Andra pradesh state in the south-eastern coastal region of India. It is the seventh-largest state by area covering an area of 162,975 km2 (62,925 sq mi) and tenth-most populous state with 49,386,799 inhabitants. It is bordered by Telangana to the north-west, Chhattisgarh to the north, Odisha to the north-east, Tamil Nadu to the south, Karnataka to the west and the Bay of Bengal to the east. It has the second longest coastline in India after Gujarat, of about 974 km (605 mi). Andhra Pradesh was the first state to be formed on a linguistic basis in India on 1 October 1953. On 1 November 1956, Andhra State was merged with the Telugu-speaking areas of the Hyderabad State to form Andhra Pradesh. Amaravati serves as the capital with the largest city being Visakhapatnam.<br><br>

People of the said era supported local art and culture by building temples and sculptures of the Buddhist monuments in the state.It was ruled by the Mauryan Empire, Satavahana dynasty, Salankayanas, Andhra Ikshvakus, Pallavas, Vishnukundinas, Eastern Chalukyas, Rashtrakutas, Cholas, Kakatiyas, Vijayanagara Empire, Gajapati Empire, Mughal Empire, Deccan sultanates, Qutb Shahi dynasty, and Asaf Jahis. In the 3rd century BCE, Andhra was a vassal kingdom of Ashoka, but after his death Andhra became powerful and extended its empire to the whole of Maratha country and beyond.<br><br>

The Andhra Pradesh government headed by chief minister YS Jagan Mohan Reddy has issued a notification setting up new districts in AP. The notification was issued specifying the revenue boundaries and district centres of the total 26 districts. The notification said that 26 districts would be formed instead of 13 districts and announced the same old names for the districts now formed with district centres. The new districts include Manyam, Alluri Seetarama Raju, NTR, Anakapalli, Konaseema, Eluru, Bapatla, Palnadu, Nandyala, Sri Satyasai, Kakinada, Annamaiah, Sri Balaji and Parvathipuram. <br><br>
<img src="image/apnew.jpg"><br>
</i></p>
<h2 id = "culture"style = "color:red">CULTURE</h2>
<p style = "font-size:200%"><i>The rich cultural heritage of Andhra Pradesh is evident in the internationally renowned Carnatic music. The region is home to some of the most popular classical musicians like Thyagaraja and Annamacharya. In addition, the state's popular form of dance, Kuchipudi, showcases immense talent and creativity.</i></p>
<h3 style = "color:blue">KUCHIPUDI</h3>
<img src="image/220px-Kuchipudi.jpg"style="float:right"><br>
<p style = "font-size:200%"><i>ayapa Senani (Jayapa Nayudu) was the first known author to write about the dance forms prevalent in Andhra Pradesh. Both Desi and Margi forms of dance have been included in his Sanskrit treatise Nritya Ratnavali, which contains eight chapters. Folk dance forms like Perani, Prenkhana, Suddha Nartana, Carcari, Rasaka, Danda Rasaka, Shiva Priya, Kanduka Nartana, Bhandika Nrityam, Carana Nrityam, Chindu, Gondali and Kolatam are described. In the first chapter the author discusses the differences between Marga and Desi, Tandava and Lasya, Natya and Nritta.<br><br>
 Kuchipudi is the best-known classical dance form of Andhra Pradesh. The various dance forms that existed through the state's history are Chenchu Bhagotam, Kuchipudi, Bhamakalapam, Burrakatha, Veeranatyam, Butta bommalu, Dappu, Tappeta Gullu, Dhimsa, and Kolaatam.</i></p>
<h3 style = "color:blue">Handicrafts </h3>
<img src="image/puppets.jpeg" width = "50%" height ="50%"><br>
<p style = "font-size:200%"><i>Since a long time, India has been identified by its traditional handicraft items occupying a special place in the heart. Andhra Pradesh has been able to keep that spirit alive and is yet another place to offer some exotic and alluring handicraft designs to its visitors. This has been possible with the training that has been given to the involved professionals with care and dedication. The people of the state are in love with their culture and their work itself showcases it. The handicraft culture of Andhra Pradesh is known for the impeccable Banjara embroidery, wood carving and metalwork.</i></p>
<h3 style = "color:blue">Architecture</h3>
<img src="image/literature.jpg"width = "50%" height ="50%"><br>
<p style = "font-size:200%"><i>There are two distinct architectural traditions in Andhra Pradesh. The first traces back to the building of the city of Amaravati under Satavahanas. This unique style of architecture emphasises the use of intricate and abstract sculpture with inspiration from religious themes. The second tradition draws on the enormous granite and limestone reserves of the region and is reflected in the various temples and forts built over a very long period of time.</i></p> 
<h3 style = "color:blue">Literature</h3>
<img src="image/maxresdefault.jpg"width = "50%" height ="50%"><br>
<p style = "font-size:200%"><i>As an ancient language, Telugu has a rich and deep literary culture. Nannaya, Tikkana, Yerrapragada, Srinatha, Molla, and Tarikonda Venkamamba made the Telugu language alluring- a lingua franca for religion, musical composition and philosophy. The contributions of Charles Phillip Brown, Vemana, Sri Sri and Viswanatha Satyanarayana made Telugu a vibrant and evolving modern language. The contributions of various Telugu/Tamil/Sanskrit grammarians to the formalisation of English grammar gave Telugu literary traditions a truly global reach. </i></p>
<h3 style = "color:blue">Cuisine</h3>
<img src="image/mango-pickle-recipe.jpg"width = "50%" height ="50%"><br>
<p style = "font-size:200%"><i>The Andhra Pradesh cuisine includes bandar laddu, avakaya, gongura, pulusu, pappucharu, jonna kudu, bobbattu, kaja, and arisa. It uses spices, fruit and vegetable harvests of the region.

Rice is the staple food and is used in a wide variety of ways. Typically, rice is either boiled and eaten with curry, or made into a batter for use in a crepe-like dish called attu (pesarattu) or dosas. The rice is usually eaten with pappu, a simple curry. Pesarattu is different from attu and dosas; it has sprouts ground into the batter, making it green.

Meat, vegetables, and greens are prepared with different masalas into a variety of strongly flavored dishes.</i></p><br><br>
